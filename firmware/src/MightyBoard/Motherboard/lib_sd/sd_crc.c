uint16_t sd_crc16(const uint8_t *data, uint16_t len) {
    uint16_t crc = 0;
    while (len--)
	crc = (crc << 8) ^
	    pgm_read_word(&crc16_table[((crc >> 8) ^ (*data++)) & 0xFF]);
    return crc;
}
